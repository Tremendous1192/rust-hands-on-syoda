fn main() {
    // Create a variable 'num' with the value 1234
    let num = 1234;

    // Copy the value of 'num' to a new variable 'num2' (primitive types are Copy)
    let num2 = num;

    // Print the original variable 'num'
    println!("num: {}", num);

    // Print the copied variable 'num2'
    println!("num2: {}", num2);
}
